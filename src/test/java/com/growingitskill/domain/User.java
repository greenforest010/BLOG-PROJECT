package com.growingitskill.domain;

public class User {
	
	private String id;
	private String tname;
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getId() {
		return id;
	}
	
	public void setTname(String tname) {
		this.tname = tname;
	}
	
	public String getTname() {
		return tname;
	}

}
